# Laço for
#
for c in range(0, 4):  # o primeiro numero é onde começa a contagem e o ultimo numero da contagem é ignorado.
    print(c)
print('FIM')

for c in range(0, 4, 2):  # Pulando de 2 em 2
    print(c)
print('FIM')

for c in range(4, 0, -1):  # contando de traz para frente
    print(c)
print('FIM')
